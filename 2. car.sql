-- Penggunaan SUM,AVG,MIN,MAX
-- Menampilkan nilai Maksimal hanya 1 output
SELECT MIN(price) FROM car;
-- Menampilkan nilai Minimal hanya 1 output 
SELECT MAX(price) FROM car;
-- Menampilkan Rata-rata nilai dari kolom yang di pilih
SELECT AVG(price) FROM car;
-- Menampilkan penambahan keseluruhan dari suatu kolom dengan menggunakan GROUP BY
SELECT make,SUM(price) FROM CAR GROUP BY make;

-- Menampilkan beberapa kolom dan menambahkan 1 nilai dengan kolom nya di ganti dengan AS
-- AS = Alias
SELECT id,make,price,ROUND(price*.75,2) AS price_10perc FROM car;

-- Penggunaan COALESCE
-- Mengganti nilai yang NULL/Kosong menjadi bentuk = 'Email not provided'
SELECT COALESCE(email, 'Email not provided') FROM person;

-- Penggunaan NULLIF
-- Di input oleh 2 argument dan akan mengembalikan ke argument 1 jika argument 1 != argument 2
SELECT NULLIF(10,10); --Output tidak ada
SELECT NULLIF(10,1); --Output 10
SELECT NULLIF(100,1); --Output 100

-- Menampilkan Waktu sekarang
SELECT NOW();
-- Melihat Tanggal saja
SELECT NOW()::DATE;

-- Adding and Subtracting with Dates Bisa menggunakan - dan +
-- Penggunaan Interval
SELECT NOW() - INTERVAL '10 YEARS';
SELECT NOW() + INTERVAL '10 MONTHS';
SELECT NOW() - INTERVAL '10 DAYS';
-- Jika hanya mau melihat tanggal
SELECT (NOW() + INTERVAL '20 YEARS')::DATE;

-- Penggunaan Extract
SELECT EXTRACT(YEAR FROM NOW());
SELECT EXTRACT(MONTH FROM NOW());
SELECT EXTRACT(DAY FROM NOW());
SELECT EXTRACT(CENTURY FROM NOW());
SELECT EXTRACT(SECOND FROM NOW());
SELECT EXTRACT(HOUR FROM NOW());
SELECT EXTRACT(MINUTE FROM NOW());